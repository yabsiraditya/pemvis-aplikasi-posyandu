    protected void datatable(){
        Object[] Baris = {"Kode Pembayaran","Tanggal Pembayaran","Total Harga","Bayar","Kembalian","ID Transaksi"};
        tabmode = new DefaultTableModel (null, Baris);
        String findItem = k_cari.getText();
        try {
            String sql = "SELECT * FROM pembayaran where `kdpembayaran` like '%" + findItem + "%' or `tgl_bayar` like '%" + findItem + "%' or `id_transaksi` like '%" + findItem + "%' order by kdpembayaran asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    "Rp" + hasil.getString(3),
                    "Rp" + hasil.getString(4),
                    "Rp" + hasil.getString(5),
                    hasil.getString(6),
                });
                
            }
            Tbalita.setModel(tabmode); 
        }catch (Exception e){
             JOptionPane.showMessageDialog(null,"datatable gagal "+e);
           }
        
         }
