    private void btn_cekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cekActionPerformed
        // TODO add your handling code here:
          if(k_pembayaran.getText() != null && !k_pembayaran.getText().isEmpty() && !k_pembayaran.getText().equals("")) {
        try {
         
                 String sql = "SELECT * from pemeriksaan where kdpembayaran = '" + k_pembayaran.getText() + "'"; 
                 java.sql.Statement stat = conn.createStatement();
                    ResultSet hasil = stat.executeQuery(sql);
                     if(!hasil.isBeforeFirst()) {
                        JOptionPane.showMessageDialog(null, "Kode tidak ditemukan, silahkan masukkan kembali kode yang benar");
                        k_pembayaran.setText("");
                        btn_simpan.setEnabled(false);
                     } else {
                         hasil.next();
                         k_totalbayar.setText(hasil.getString("harga"));
                        String sql2 = "SELECT kdpembayaran from pembayaran where kdpembayaran ='" + k_pembayaran.getText() + "'";
                        stat = conn.createStatement();
                        ResultSet hasil2 = stat.executeQuery(sql2);
                        if(!hasil2.isBeforeFirst()) {
                             JOptionPane.showMessageDialog(null, "Kode Pembayaran valid");
                             btn_simpan.setEnabled(true);
                        } else {
                            hasil2.next();
                                if(k_pembayaran.getText().equals(hasil2.getString(1))) {
                                    JOptionPane.showMessageDialog(null, "Kode Pembayaran sudah dibayar!");
                                    kosong();
                                    btn_simpan.setEnabled(false);
                                }
                        }
                     }
                     
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
          } else {
                JOptionPane.showMessageDialog(null, "Masukkan Kode Pembayaran!");
          }
    }//GEN-LAST:event_btn_cekActionPerformed

